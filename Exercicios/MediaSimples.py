# Objetivo: Faça um Programa que receba dois números, calcule a média e imprima na tela.
# Entrada: Dois números.
# Saida: Média.
# Autor: Rafael Florentino.

nota1 = float(input('Digite uma Nota: '))
nota2 = float(input('Digite outro nota: '))
media = (nota1 + nota2) / 2

print('A média é: {:.1f}'.format(media))




